  <p class="paragrafo-eventos6">
  A Apresentação sobre o Dia Do Folclore foi feita pela Julia, pela Marcella e pela Nicoly. 
As meninas começaram explicando em que dia que se comemora o dia do Folclore e porque ele é comemorado.
Elas falaram sobre a importância do Folclore e os personagens mais lembrados do nosso Folclore.
No final da apresentação tivemos 2 dinâmicas, a primeira foi feita pela Franciele e pela Ana Paula, já a segunda foi feita pela Ana Flávia.
A primeira dinâmica foi dividida em duas partes a 1ª foi uma brincadeira de forca onde a palavra era “Tupi-Guarani”. A 2ª parte foi uma corrida de uma perna só, onde três pessoas de cada setor tinham que se revezar, no final foi revelado que o vencedor na verdade era quem chegasse por último, já que o Saci adorava pregar peças.
Já a segunda dinâmica foi um quiz de verdade ou mentira sobre o Folclore.<br><br><br>
O Dia do Folclore, comemorado em 22 de agosto, foi criado com o objetivo de incentivar estudos para preservar o acervo do folclore brasileiro.
No Brasil, o Dia do Folclore foi oficializado em 17 de agosto de 1965, por meio do Decreto nº 56.747.
  </p>
  <h4 class="titulo-eventos7" style="font-size:large; font-family:-apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, Oxygen, Ubuntu, Cantarell, 'Open Sans', 'Helvetica Neue', sans-serif; padding:20px;">Importância do Folclore Brasileiro</h4>
  <p class="paragrafo-eventos7"> 
  Atualmente, o folclore brasileiro é um importante objeto de estudo nas ciências humanas, e sua importância é reforçada frequentemente nas escolas, sobretudo naquelas que trabalham com ensino infantil.
No Dia do Folclore, costuma-se relembrar os elementos mais importante da cultura popular brasileira, tais como as danças, os ritmos, as festas e os personagens do nosso folclore. Nas danças e ritmos, podem ser citados o frevo, o maracatu, o baião, o forró, a catira etc. 
  </p>
